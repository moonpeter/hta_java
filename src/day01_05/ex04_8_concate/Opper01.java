package day01_05.ex04_8_concate;

public class Opper01 {
	public static void main(String args[]) {
		
		int a = 10, b = 4;
		
		System.out.println(a + "+" + b + "=" +(a+b));
		
		System.out.println(a + "-" + b + "=" +(a-b));

		System.out.println(a + "*" + b + "=" +(a*b));
		
		System.out.println(a + "/" + b + "=" +(a/b));
		
		System.out.println(a + "%" + b + "=" +(a%b));
	}
}
