package day01_05.ex06_1119;

public class ForExample01_1 {
	public static void main(String[] args) {
		for (int cnt = 0; cnt < 10; cnt+=1) {
			System.out.println(cnt);
		}
		System.out.println("End~~~!!!!");
		
		for (int cnt = 9; cnt >= 0; cnt-=1) {
			System.out.println(cnt);
		}
		System.out.println("End~~~!!!!");
	}
}
