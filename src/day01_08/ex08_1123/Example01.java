package day01_08.ex08_1123;

public class Example01 {
	public static void main(String args[]) {
		int idx = 30;
		
		for (int i = 0; i < idx; i++) {
			System.out.print("*");
		}
		System.out.println();
		
		for (int i = 0; i < idx; i++) {
			System.out.print("-");
		}
		
		System.out.println();
		System.out.println("Hello, Java");
		
		for (int i = 0; i < idx; i++) {
			System.out.print("-");
		}
		System.out.println();
		
		for (int i = 0; i < idx; i++) {
			System.out.print("*");
		}
	}
}
